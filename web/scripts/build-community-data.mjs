#!/usr/bin/env node
/**
 * Build community prototype data from .community/manifest.json + ingest projections.
 * Run: node web/scripts/build-community-data.mjs
 */
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

import { REPO, readJson } from './lib/provenance.mjs';

const MANIFEST = join(REPO, '.community/manifest.json');
const MEDIA_GEN = join(REPO, 'web/src/lib/data/media/generated.ts');
const SHOWCASE_GEN = join(REPO, 'web/src/lib/data/showcase/lenny-batch.generated.ts');
const OUT = join(REPO, 'web/src/lib/data/community/generated.ts');

if (!existsSync(MANIFEST)) {
	console.log(`Skip community build — no .community/manifest.json; keeping committed ${OUT}`);
	process.exit(0);
}

/** Pull episode titles/summaries for agent RAG stub (no vector DB). */
function agentSnippetsFromProjections() {
	const snippets = [];

	if (existsSync(MEDIA_GEN)) {
		const src = readFileSync(MEDIA_GEN, 'utf8');
		const match = src.match(/episodes:\s*\[([\s\S]*?)\]\s*as const/);
		if (match) {
			const titleRe = /title:\s*'((?:\\'|[^'])*)'/g;
			const guestRe = /guest:\s*'((?:\\'|[^'])*)'/g;
			const summaryRe = /summary:\s*\n?\s*'((?:\\'|[^'])*)'/g;
			const block = match[1];
			const titles = [...block.matchAll(titleRe)].map((m) => m[1].replace(/\\'/g, "'"));
			const guests = [...block.matchAll(guestRe)].map((m) => m[1].replace(/\\'/g, "'"));
			const summaries = [...block.matchAll(summaryRe)].map((m) => m[1].replace(/\\'/g, "'"));
			for (let i = 0; i < Math.min(titles.length, 12); i++) {
				snippets.push({
					id: `media-${i}`,
					source: 'media',
					title: titles[i] ?? 'Episode',
					guest: guests[i] ?? '',
					text: summaries[i] ?? ''
				});
			}
		}
	}

	if (existsSync(SHOWCASE_GEN)) {
		const src = readFileSync(SHOWCASE_GEN, 'utf8');
		const shiftOld = [
			...src.matchAll(/landingSlot:\s*'shift-old'[\s\S]*?title:\s*'((?:\\'|[^'])*)'/g)
		].map((m) => m[1].replace(/\\'/g, "'"));
		for (const title of shiftOld.slice(0, 5)) {
			snippets.push({
				id: `shift-${snippets.length}`,
				source: 'showcase',
				title,
				guest: 'Lenny batch',
				text: `Belief we teach against (shift-old): ${title}`
			});
		}
	}

	snippets.push({
		id: 'funcode-core',
		source: 'chain',
		title: 'FunCode mental model',
		guest: 'FunCode',
		text: 'PRs are a playground, not a gate. The codebase is a shared sandbox — build in the open, ship what works, delete or hand off the rest. Every rung of GLO-9 is free.'
	});

	return snippets;
}

const manifest = readJson(MANIFEST);
const agentSnippets = agentSnippetsFromProjections();

const out = `/** Auto-generated by web/scripts/build-community-data.mjs — do not edit. */
import type { CommunityManifest } from './types';

export const communityGenerated = ${JSON.stringify({ ...manifest, agentSnippets }, null, '\t')} as const satisfies CommunityManifest;
`;

writeFileSync(OUT, out);
console.log(
	`Wrote ${OUT} — ${manifest.programs.length} programs, ${manifest.labels.length} labels, ${manifest.collections.length} collections, ${manifest.shareGrants.length} share grants, ${agentSnippets.length} agent snippets`
);
